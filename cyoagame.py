print("There once was a prince named Jacob.")
print("He was looking far and wide to find a dragon to slay.")
print("However, he has not found the dragon yet.")
print("He is now stuck in a maze of twisty passages. And he is presented with a choice of turning left or right.")

choice = input("Which way should he go? Press l for left or r for right: ")

if choice == "l":
    print("He turned left and found the dragon.")
    print("He slayed the dragon and returned to his home.")
    print("He is now very happy.")

if choice == "r":
    print("He turned right and found a trap!")
    print("He fell to his death.")